#include <iostream>

using namespace std;

int main() {
    int integerVariable = 10;
    float floatVariable = 3.14;
    char charVariable = 'A';
    double doubleVariable = 2.71828;

    cout << "Integer: " << integerVariable << endl;
    cout << "Float: " << floatVariable << endl;
    cout << "Character: " << charVariable << endl;
    cout << "Double: " << doubleVariable << endl;
    return 0;
}
