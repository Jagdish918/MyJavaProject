public class datatype {
        public static void main(String[] args) {
            int integerVariable = 10;
            float floatVariable = 3.14f;
            char charVariable = 'A';
            double doubleVariable = 2.71828;
            boolean Var = true;
    
            System.out.println("Integer: " + integerVariable);
            System.out.println("Float: " + floatVariable);
            System.out.println("Character: " + charVariable);
            System.out.println("Double: " + doubleVariable);
            System.out.println("boolean : " + Var);
        }
    }
    

